    private function normalizeText($text)
    {
        // تحويل إلى حروف صغيرة
        $text = mb_strtolower($text, 'UTF-8');
        
        // توحيد أشكال الألف
        $text = str_replace(['أ', 'إ', 'آ', 'ٱ'], 'ا', $text);
        
        // توحيد تاء مربوطة وهاء
        $text = str_replace('ة', 'ه', $text);
        
        // إزالة التشكيل
        $text = preg_replace('/[ًٌٍََُُِِّّْْٰٓٔ]/u', '', $text);
        
        // إزالة الرموز الخاصة
        $text = preg_replace('/[^\p{L}\p{N}\s]/u', ' ', $text);
        
        // إزالة المسافات الزائدة
        $text = preg_replace('/\s+/', ' ', $text);
        
        return trim($text);
    }
    
    // دالة مساعدة لمعرفة سبب عدم التطابق (للتجريب)
    public function debug(Request $request)
    {
        $question = $request->input('message');
        $normalized = $this->normalizeText($question);
        $words = str_word_count($normalized, 1, 'ءآأؤإئة');
        
        $results = [];
        
        foreach ($this->knowledgeBase as $key => $item) {
            if (empty($item['keywords'])) continue;
            
            $matches = [];
            foreach ($item['keywords'] as $keyword) {
                $normalizedKeyword = $this->normalizeText($keyword);
                
                if (strpos($normalized, $normalizedKeyword) !== false) {
                    $matches[] = "مطابقة مباشرة: $keyword";
                }
                
                foreach ($words as $word) {
                    similar_text($word, $normalizedKeyword, $percent);
                    if ($percent > 70) {
                        $matches[] = "تشابه ($percent%) بين '$word' و '$keyword'";
                    }
                }
            }
            
            if (!empty($matches)) {
                $results[$key] = $matches;
            }
        }
        
        return response()->json([
            'original' => $question,
            'normalized' => $normalized,
            'words' => $words,
            'matches' => $results
        ]);
    }
